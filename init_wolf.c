#include "wolf3d.h"

t_data	*init_wolf(void)
{
	t_data	*t;

	t = (t_data *)ft_memalloc(sizeof(t_data));
	t->posx = 3;
	t->posy = 3;
	t->dirx = -1;
	t->diry = 0;
	t->planx = 0;
	t->plany = 0.66;
	t->ms = 0.2;
	t->rs = 0.1;
	t->onoff = 0;
	return (t);
}
