void		save_map(t_data *t, int mapp[22][22])
{
	int		i;
	int		j;

	i = 0;
	while (i < 22)
	{
		j = 0;
		while (j < 22)
		{
			t->map[i][j] = mapp[i][j];
			j++;
		}
		i++;
	}
}

